// Calculate total chapters
const getTotalChapters = () => {
  let total = 0;
  MATERI_INDEX.forEach(bagian => {
    total += bagian.chapters.length;
  });
  return total;
};

// State
let currentFilter = 'all';
let searchQuery = '';

// DOM Elements
const bagianGrid = document.getElementById('bagianGrid');
const filterChips = document.querySelectorAll('.chip');
const searchInput = document.getElementById('searchInput');
const dashboardStats = document.getElementById('dashboardStats');

// Init
function init() {
  renderStats();
  renderGrid();
  bindEvents();
}

function renderStats() {
  dashboardStats.innerHTML = `
    <div class="stat-badge"><span>${MATERI_INDEX.length}</span> Bagian</div>
    <div class="stat-badge"><span>${getTotalChapters()}</span> Materi</div>
  `;
}

function bindEvents() {
  filterChips.forEach(chip => {
    chip.addEventListener('click', (e) => {
      // Update active class
      filterChips.forEach(c => c.classList.remove('active'));
      e.target.classList.add('active');
      
      currentFilter = e.target.dataset.filter;
      renderGrid();
    });
  });

  searchInput.addEventListener('input', (e) => {
    searchQuery = e.target.value.toLowerCase();
    renderGrid();
  });
}

function renderGrid() {
  bagianGrid.innerHTML = '';
  
  MATERI_INDEX.forEach(bagian => {
    // Filter by category
    if (currentFilter !== 'all' && bagian.category !== currentFilter) {
      return;
    }
    
    // Filter by search query
    let matchingChapters = bagian.chapters;
    let matchesTitle = bagian.title.toLowerCase().includes(searchQuery);
    
    if (searchQuery) {
      matchingChapters = bagian.chapters.filter(ch => ch.title.toLowerCase().includes(searchQuery));
      if (!matchesTitle && matchingChapters.length === 0) {
        return;
      }
    }
    
    const card = document.createElement('div');
    card.className = `bagian-card ${bagian.category === 'advanced' ? 'advanced' : ''}`;
    
    let chaptersHtml = '';
    matchingChapters.forEach(ch => {
      chaptersHtml += `
        <div class="chapter-item">
          <div class="chapter-title">${ch.title}</div>
          <a href="editor.html?folder=${bagian.folder}&file=${ch.file}" class="btn btn-ghost btn-sm">Edit</a>
        </div>
      `;
    });
    
    card.innerHTML = `
      <div class="bagian-header" onclick="this.parentElement.classList.toggle('expanded')">
        <div class="bagian-num">${String(bagian.num).padStart(2, '0')}</div>
        <div class="bagian-info">
          <div class="bagian-title">${bagian.title}</div>
          <div class="bagian-stats">
            <span>${bagian.chapters.length} Materi</span>
          </div>
        </div>
      </div>
      <div class="chapter-list">
        ${chaptersHtml}
      </div>
    `;
    
    bagianGrid.appendChild(card);
  });
}

// Start
document.addEventListener('DOMContentLoaded', init);
